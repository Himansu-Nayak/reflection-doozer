package com.org.dozer.type1.src;

/**
 * The Class Address is used as a source bean for Address
 */
public class Address {

	/** The door no. */
	private int doorNo;

	/** The st name. */
	private String stName;

	/**
	 * Gets the door no.
	 * 
	 * @return the door no
	 */
	public int getDoorNo() {
		return doorNo;
	}

	/**
	 * Sets the door no.
	 * 
	 * @param doorNo
	 *            the new door no
	 */
	public void setDoorNo(int doorNo) {
		this.doorNo = doorNo;
	}

	/**
	 * Gets the st name.
	 * 
	 * @return the st name
	 */
	public String getStName() {
		return stName;
	}

	/**
	 * Sets the st name.
	 * 
	 * @param stName
	 *            the new st name
	 */
	public void setStName(String stName) {
		this.stName = stName;
	}

}
